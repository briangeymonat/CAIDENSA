                    else return false;
                case "Grupo 3":
                    if (ElItinerario.lstBeneficiarios.Count == 3) return true;
                    else return false;
                case "Taller":
                    if (ElItinerario.lstBeneficiarios.Count == 4) return true;
                    else return false;
