                    if (ElItinerario.lstBeneficiarios.Count <= 8 && ElItinerario.lstBeneficiarios.Count >= 5 && ElItinerario.lstEspecialistas.Count==2) return true;
                    else return false;
                default:
                    return false;
            }
        }
