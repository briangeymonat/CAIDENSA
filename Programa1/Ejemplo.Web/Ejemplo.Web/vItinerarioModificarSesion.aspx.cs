        protected void btnConfirmar_Click(object sender, EventArgs e)
        {
            if(!FaltanDatos())
            {
                if(!VerificarCantidadAgregados())
                {
                    ClientScript.RegisterClientScriptBlock(GetType(), "alert", "alert('ERROR: El número de beneficiarios no coincide con el tipo de sesión.')", true);
                }
                else
                {
                    //              ↓↓↓↓↓↓↓CARGA DE DATOS NUEVOS↓↓↓↓↓↓

                    //TIPO DE SESION
                    switch (ddlTipoSesion.SelectedValue.ToString())
                    {
                        case "Individual":
                            ElItinerario.TipoSesion = cUtilidades.TipoSesion.Individual;
                            break;
                        case "Grupo 2":
                            ElItinerario.TipoSesion = cUtilidades.TipoSesion.Grupo2;
                            break;
                        case "Grupo 3":
                            ElItinerario.TipoSesion = cUtilidades.TipoSesion.Grupo3;
                            break;
                        case "Taller":
                            ElItinerario.TipoSesion = cUtilidades.TipoSesion.Taller;
                            break;
                        case "PROES":
                            ElItinerario.TipoSesion = cUtilidades.TipoSesion.PROES;
                            break;
                    }

                    //DIA
                    switch (ddlDias.SelectedValue.ToString())
                    {
                        case "Lunes":
                            ElItinerario.Dia = "L";
                            break;
                        case "Martes":
                            ElItinerario.Dia = "M";
                            break;
                        case "Miercoles":
                            ElItinerario.Dia = "X";
                            break;
                        case "Jueves":
                            ElItinerario.Dia = "J";
                            break;
                        case "Viernes":
                            ElItinerario.Dia = "V";
                            break;
                        case "Sabado":
                            ElItinerario.Dia = "S";
                            break;
                    }

                    //CENTRO
                    if (rdCentro.SelectedIndex == 0) ElItinerario.Centro = cUtilidades.Centro.JuanLacaze;
                    else ElItinerario.Centro = cUtilidades.Centro.NuevaHelvecia;

                    if (DateTime.Parse(ddlDesde.SelectedValue) >= DateTime.Parse(ddlHasta.SelectedValue))
                    {
                        ClientScript.RegisterClientScriptBlock(GetType(), "alert", "alert('ERROR: La hora de fin de la sesión debe ser mayor a la de inicio.')", true);
                    }
                    else
                    {
                        // HORARIO
                        ElItinerario.HoraInicio = ddlDesde.SelectedValue;
                        ElItinerario.HoraFin = DateTime.Parse(ddlHasta.SelectedValue).AddMinutes(-1).ToShortTimeString();

                        // PLANES DE LOS BENEFICIARIOS
                        for (int i = 0; i < ElItinerario.lstBeneficiarios.Count; i++)
                        {
                            ElItinerario.lstBeneficiarios[i].Plan = LosPlanes[i];
                            switch (i)
                            {
                                case 0:
                                    ElItinerario.lstBeneficiarios[i].Plan = ElItinerario.lstBeneficiarios[i].Beneficiario.lstPlanes[ddlPlan1.SelectedIndex];
                                    break;
                                case 1:
                                    ElItinerario.lstBeneficiarios[i].Plan = ElItinerario.lstBeneficiarios[i].Beneficiario.lstPlanes[ddlPlan2.SelectedIndex];
                                    break;
                                case 2:
                                    ElItinerario.lstBeneficiarios[i].Plan = ElItinerario.lstBeneficiarios[i].Beneficiario.lstPlanes[ddlPlan3.SelectedIndex];
                                    break;
                                case 3:
                                    ElItinerario.lstBeneficiarios[i].Plan = ElItinerario.lstBeneficiarios[i].Beneficiario.lstPlanes[ddlPlan4.SelectedIndex];
                                    break;
                                case 4:
                                    ElItinerario.lstBeneficiarios[i].Plan = ElItinerario.lstBeneficiarios[i].Beneficiario.lstPlanes[ddlPlan5.SelectedIndex];
                                    break;
                                case 5:
                                    ElItinerario.lstBeneficiarios[i].Plan = ElItinerario.lstBeneficiarios[i].Beneficiario.lstPlanes[ddlPlan6.SelectedIndex];
                                    break;
                                case 6:
                                    ElItinerario.lstBeneficiarios[i].Plan = ElItinerario.lstBeneficiarios[i].Beneficiario.lstPlanes[ddlPlan7.SelectedIndex];
                                    break;
                                case 7:
                                    ElItinerario.lstBeneficiarios[i].Plan = ElItinerario.lstBeneficiarios[i].Beneficiario.lstPlanes[ddlPlan8.SelectedIndex];
                                    break;
                                default:
                                    break;
                            }
                        }
                        // COMENTARIO
                        ElItinerario.Comentario = txtComentario.Text;

                        // VERIFICARO HORARIO DE ESPECIALISTAS Y BENEFICIARIOS
                        List<cUsuario> lstEspecialistasNoDisponibles = dFachada.ItinerarioVerificarHorarioUsuarioModificar(ElItinerario);
                        List<cBeneficiario> lstBeneficiariosNoDisponibles = dFachada.ItinerarioVerificarHorarioBeneficiariosModificar(ElItinerario);
                        string sEspecialistas = "";
                        string sBeneficiarios = "";

                        //ESPECIALISTAS NO DISPONIBLES
                        if (lstEspecialistasNoDisponibles.Count > 0)
                        {
                            if (lstEspecialistasNoDisponibles.Count > 1)
                            {
                                sEspecialistas += "Los especialistas ";
                            }
                            for (int i = 0; i < lstEspecialistasNoDisponibles.Count; i++)
                            {
                                if (i == lstEspecialistasNoDisponibles.Count - 1)
                                {
                                    sEspecialistas += lstEspecialistasNoDisponibles[i].Nombres + " " + lstEspecialistasNoDisponibles[i].Apellidos;
                                }
                                else if (i == 0)
                                {
                                    sEspecialistas += lstEspecialistasNoDisponibles[i].Nombres + " " + lstEspecialistasNoDisponibles[i].Apellidos + ", ";
                                }
                                else if (i == lstEspecialistasNoDisponibles.Count - 2)
                                {
                                    sEspecialistas += lstEspecialistasNoDisponibles[i].Nombres + " " + lstEspecialistasNoDisponibles[i].Apellidos + " y ";
                                }
                            }
                            if (lstEspecialistasNoDisponibles.Count > 1)
                            {
                                sEspecialistas += " no están disponibles para la sesión.";
                            }
                            else
                            {
                                sEspecialistas += " no está disponible para la sesión.";
                            }
                        }

                        //BENEFICIARIOS NO DISPONIBLES

                        if (lstBeneficiariosNoDisponibles.Count > 0)
                        {
                            if (lstEspecialistasNoDisponibles.Count > 1)
                            {
                                sBeneficiarios += "Los beneficiarios ";
                            }
                            for (int i = 0; i < lstBeneficiariosNoDisponibles.Count; i++)
                            {
                                if (i == lstBeneficiariosNoDisponibles.Count - 1)
                                {
                                    sBeneficiarios += lstBeneficiariosNoDisponibles[i].Nombres + " " + lstBeneficiariosNoDisponibles[i].Apellidos;
                                }
                                else if (i == 0)
                                {
                                    sBeneficiarios += lstBeneficiariosNoDisponibles[i].Nombres + " " + lstBeneficiariosNoDisponibles[i].Apellidos + ", ";
                                }
                                else if (i == lstBeneficiariosNoDisponibles.Count - 2)
                                {
                                    sBeneficiarios += lstBeneficiariosNoDisponibles[i].Nombres + " " + lstBeneficiariosNoDisponibles[i].Apellidos + " y ";
                                }
                            }
                            if (lstBeneficiariosNoDisponibles.Count > 1)
                            {
                                sBeneficiarios += " no están disponibles para la sesión.";
                            }
                            else
                            {
                                sBeneficiarios += " no está disponible para la sesión.";
                            }
                        }
                        if (lstEspecialistasNoDisponibles.Count > 0 || lstBeneficiariosNoDisponibles.Count > 0)
                        {
                            ClientScript.RegisterClientScriptBlock(GetType(), "alert", string.Format("alert('ERROR: {0}{1} Su horario coincide con el de otra sesión.')", sEspecialistas, sBeneficiarios), true);
                        }
                        else
                        {
                            if (dFachada.ItinerarioModificar(ElItinerario)) { bModificando = false; CargarTodo(); dFachada.SesionAgregarSesionesDelDia(); }
                            else
                            {
                                ClientScript.RegisterClientScriptBlock(GetType(), "alert", "alert('ERROR: No se pudo modificar la sesión del itinerario.')", true);
                            }
                        }
                    }
                }
            }
            else
            {
                ClientScript.RegisterClientScriptBlock(GetType(), "alert", "alert('ERROR: Faltan datos.')", true);
            }
        }

